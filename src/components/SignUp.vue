<template>
  <div class="container">
    <div class="row">
      <div class="col-sm-9 col-md-7 col-lg-5 mx-auto">
        <div class="card card-signin my-5">
          <div class="card-body">
            <h5 class="card-title text-center">Crie uma nova conta</h5>
            <form class="form-signin">
              <div class="form-label-group">
                <input type="email" id="inputEmail" class="form-control"
                placeholder="E-mail" v-model="email" required autofocus>
              </div>

              <div class="form-label-group">
                <input type="password" id="inputPassword"
                class="form-control" placeholder="Password" v-model="password"
                required>
              </div>

              <button class="btn btn-lg btn-primary btn-block text-uppercase"
              v-on:click="signUp">Registre-se</button>
            </form>
          </div>
        </div>
      </div>
    </div>
  </div>
</template>

<script>
import firebase from 'firebase';

export default {
  /* eslint-disable */
  name: 'signup',
  data() {
    return {
      email: '',
      password: '',
    };
  },
  methods: {
    signUp() {
      firebase.auth().createUserWithEmailAndPassword(this.email, this.password).then(
        (user) => { this.$router.replace('/login'); }).catch((err) => {
        alert(err.message);
      });
    },
  },
};
</script>

